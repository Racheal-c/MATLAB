VRLL=220;
VR=VRLL/sqrt(3);
Z=[0.15+j*2*pi*60*1.3263e-3]*40;
disp=('(a)For 0.8 power factor lagging')
SR=304.8+j*228.6;
IR=conj(SR)/(3*conj(VR));
IS=IR;
VS=VR+Z*IR;
VSLL=sqrt(3)*abs(VS)
SS=3*VS*conj(IS)
REG=((VSLL-VRLL)/(VRLL))*10
EFF=(real(SR)/real(SS))*100
disp=('(b)For 0.8 power factor leading')
SR=304.8-j*228.6;
IR=conj(SR)/(3*conj(VR));
IS=IR;
VS=VR+Z*IR;
VSLL=sqrt(3)*abs(VS)
SS=3*VS*conj(IS)
REG=((VSLL-VRLL)/(VRLL))*100
EFF=(real(SR)/real(SS))*100